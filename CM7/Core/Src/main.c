static void PA8_ForceHighEarly(void)
{
  RCC->AHB4ENR |= RCC_AHB4ENR_GPIOAEN;
  (void)RCC->AHB4ENR;

  GPIOA->MODER = (GPIOA->MODER & ~(3UL << (8U * 2U))) | (1UL << (8U * 2U));
  GPIOA->OTYPER &= ~GPIO_PIN_8;
  GPIOA->PUPDR &= ~(3UL << (8U * 2U));
  GPIOA->OSPEEDR &= ~(3UL << (8U * 2U));
  GPIOA->BSRR = GPIO_PIN_8;
}

